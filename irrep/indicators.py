def z2w_j(character_info, j=1):
    i = j - 1
    total_inv = 0.0
    for kpoint in character_info.values():
        if is_trim(np.array(kpoint["k"])) and np.isclose(kpoint["k"][i], 0.5):
            total_inv += (kpoint["characters"] < 0.0).sum()

    return total_inv % 2


def z2w_triplet(character_info):
    return [z2w_j(character_info, j) for j in range(1, 4)]


def z4(character_info):
    total_inv = 0.0j
    for kpoint in character_info.values():
        total_inv += -kpoint["characters"].sum()

    if not np.isclose(np.imag(total_inv), 0):
        raise ValueError(
            "z4 index computation resulted in imaginary index. Check inversion traces."
        )

    total_inv = np.real(total_inv)

    return (0.5 * total_inv) % 4


def z2(character_info):
    total = 0.0j
    for kpoint in character_info.values():
        total += -kpoint["characters"].sum()

    if not np.isclose(np.imag(total), 0):
        raise ValueError(
            "z4 index computation resulted in imaginary index. Check inversion traces."
        )

    total = np.real(total) / np.sqrt(2)

    return np.round(total % 2, 3)


