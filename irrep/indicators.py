def check_vector_in_array(vector, array):
    """Checks if a vector is in an array. Works with lists"""
    return np.isclose(vector, array).all(1).any()


def is_trim(kpoint):
    """Checks if kpoint is a trim. Works with vectors and arrays."""
    if kpoint.ndim == 1:
        return np.isclose(kpoint, -kpoint % 1).all()
    else:
        return np.isclose(kpoint, -kpoint % 1).all(1)


def count_trims(kpoints):
    """Counts the number of trims in a kpoint array."""
    return is_trim(kpoints).sum()


def check_kpoints(kpoints):
    """Performs checks on the kpoints to see if any are repeated."""
    seen = []
    repeated = []

    for k in kpoints:
        if len(seen) == 0:
            seen.append(k)
        elif not check_vector_in_array(k, seen):
            seen.append(k)
        else:
            if len(repeated) == 0:
                repeated.append(k)
            elif not check_vector_in_array(k, repeated):
                repeated.append(k)


    return np.array(repeated)


def is_inversion(matrix):
    """Checks if a matrix is the inversion symmetry

    Parameters
    ----------
    matrix : ndarray
        rotation matrix

    Returns
    -------
    bool
        whether the rotation is inversion
    """
    return np.isclose(matrix, -np.eye(3)).all()


def sg_has_inversion(data):
    """Checks if the spacegroup has inversion from the JSON output

    Parameters
    ----------
    data : dict
        JSON output of irrep

    Returns
    -------
    str
        key of the inversion symmetry if found, else None
    """
    symmetries = data["spacegroup"]["symmetries"]

    for num, sym in symmetries.items():
        rotation = np.array(sym["rotation matrix refUC"])
        if is_inversion(rotation):
            return int(num)
    return None


def sg_has_S4(data):
    symmetries = data["spacegroup"]["symmetries"]

    for num, sym in symmetries.items():
        if (np.isclose(sym["angle pi"], 0.5)) and (sym["inversion"]):
            return int(num)

    return None


def get_kpoint_coordinates_from_json(data, refUC=False):
    """Gets kpoint coordinates from irrep's JSON output."""
    kpoint_list = []
    for kpoint in data["characters and irreps"][0]["subspace"]["k points"]:
        if refUC:
            kpoint_list.append(kpoint["k_refUC"])
        else:
            kpoint_list.append(kpoint["k"])

    return np.array(kpoint_list)


def get_kpoint_coordinates_from_info(irreps_info, refUC=False):
    """Gets kpoint coordinates from the dict generated by get_irreps_info."""
    coordinates = np.zeros((len(irreps_info), 3))
    key = "k_refUC" if refUC else "k"
    for i, kname in enumerate(irreps_info):
        coordinates[i] = irreps_info[kname][key]

    return coordinates


def filter_irreps(irreps, precision=1e-4):
    """Filters valid irreps checking that the multiplicity is real and integer."""
    irrep_list = []
    for name, multi in irreps.items():
        multi = complex(*multi)
        if np.isclose(multi.imag, 0, atol=precision) and not np.isclose(
            multi.real, 0, atol=precision
        ):
            irrep_list.append((name, np.round(multi.real)))

    return irrep_list


def get_irreps_info(data):
    """Extracts relevant information for SI calculation from irrep's JSON.

    Parameters
    ----------
    data : dict
        JSON output from irrep
    with_character : int, optional
        symmetry index to include the character of, by default None

    Returns
    -------
    dict
        dictionary with relevant info
    """
    kpoints = data["characters and irreps"][0]["subspace"]["k points"]

    irrep_info = {}
    for kpoint in kpoints:
        kpoint_list = []
        energies = kpoint["energies_mean"]#
        dims = kpoint["dimensions"]
        coords = kpoint["k"]
        coords_refUC = kpoint["k_refUC"]
        name = kpoint["kpname"]

        if name is None:
            continue

        for i, irreps in enumerate(kpoint["irreps"]):
            valid_irreps = filter_irreps(irreps)
            if len(valid_irreps) > 0:
                kpoint_list.append((energies[i], valid_irreps, dims[i]))
        irrep_info[name] = {"k": coords, "k_refUC": coords_refUC, "irreps": kpoint_list}

    return irrep_info


def get_character_info(data, symop_index):
    kpoints = data["characters and irreps"][0]["subspace"]["k points"]
    char_info = {}
    unknown_index = 0
    for kpoint in kpoints:
        characters = kpoint["characters"]
        coords = kpoint["k"]
        coords_refUC = kpoint["k_refUC"]
        name = kpoint["kpname"]
        if name is None:
            name = f"unknown{unknown_index}"
            unknown_index += 1
        try:
            op_index = kpoint["symmetries"].index(symop_index)
        except ValueError:
            continue

        kpoint_list = np.array([
            np.round(energy_chars[op_index], 3) for energy_chars in characters
            ])

        char_info[name] = {
            "k": coords,
            "k_refUC": coords_refUC,
            "characters": kpoint_list,
        }

    return char_info


def count_irreps(irrep_info, by_point=False):
    """Produce a dictionary with the irrep counts

    Parameters
    ----------
    irrep_info : dict
        dictionary generated by get_irreps_info
    by_point : bool, optional
        separate by kpoint, by default False

    Returns
    -------
    dict
        dictionary with irrep counts.
    """
    if by_point:
        irrep_count = {}
        for kpoint, info in irrep_info.items():
            kpoint_count = {}
            for irreps in [x[1] for x in info["irreps"]]:
                for name, multi in irreps:
                    kpoint_count.setdefault(name, 0.0)
                    kpoint_count[name] += multi
            irrep_count[kpoint] = {
                "k": info["k"],
                "k_refUC": info["k_refUC"],
                "counts": kpoint_count,
            }

    else:
        irrep_count = {}
        for info in irrep_info.values():
            for irreps in [x[1] for x in info["irreps"]]:
                for name, multi in irreps:
                    irrep_count.setdefault(name, 0.0)
                    irrep_count[name] += multi

    return irrep_count


def compute_invariant(irrep_counts, irrep_names, factors, mod):
    """General reduction method to compute an invariant by counting irreps

    Parameters
    ----------
    irrep_counts : dict
        plain irrep counts (not by kpoint)
    irrep_names : list
        irrep names to count
    factors : list
        factors to multiply each irrep count
    mod : int
        final modulus operation to apply to the sum

    Returns
    -------
    float
        result of the computation
    """

    counts = np.array([irrep_counts.get(name, 0.0) for name in irrep_names])
    if factors is None:
        total = counts.sum()
    else:
        factors = np.array(factors, dtype=float)
        total = (factors * counts).sum()

    if mod is None:
        return total
    else:
        return total % mod

